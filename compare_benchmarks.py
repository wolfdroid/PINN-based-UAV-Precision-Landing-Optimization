"""
compare_benchmarks.py
─────────────────────
Cross-language benchmark comparison for UAV PINN landing controller.

Reads `pinn_benchmark_python.xlsx` and `pinn_benchmark_julia.xlsx`
(generated by uav_pinn_v2_torch.py and uav_pinn_v2_julia.jl respectively)
and produces a 3-panel dark-themed `benchmark_comparison.png`.

Usage:
    python compare_benchmarks.py
    python compare_benchmarks.py --python-xlsx my_python.xlsx --julia-xlsx my_julia.xlsx
"""

import argparse
import sys

import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt
import matplotlib.gridspec as gridspec
import numpy as np

# ─── XLSX column indices (0-based) for Evaluation Results sheet ───────────────
# Both Julia and Python exports use the same column layout:
# 0:Scenario, 1:TgtX, 2:TgtY, 3:WndVx, 4:WndVy,
# 5:FinalX, 6:FinalY, 7:FinalZ, 8:FinalVx, 9:FinalVy, 10:FinalVz,
# 11:PosErr, 12:VelErr, 13:T, 14:RK4Max, 15:RK4Mean
COL_SCENARIO = 0
COL_POS_ERR  = 11
COL_VEL_ERR  = 12
C_BG    = '#0A0A0F'
C_PANEL = '#12131A'
C_RED1  = '#FF2D2D'
C_SILVER = '#A8B0BC'
C_GOLD  = '#C9A84C'
C_STEEL = '#6B7A8D'
C_TEXT  = '#D8D8DC'
C_GRID  = '#1E2030'


# ─── XLSX helpers ─────────────────────────────────────────────────────────────

def load_xlsx(path):
    """Load an XLSX workbook; return None with a warning if not found."""
    try:
        import openpyxl
    except ImportError:
        print("  [ERROR] openpyxl is not installed. Install it with: pip install openpyxl")
        sys.exit(1)
    try:
        return openpyxl.load_workbook(path, data_only=True)
    except FileNotFoundError:
        print(f"  [WARN] File not found: {path}")
        return None
    except Exception as exc:
        print(f"  [WARN] Could not read {path}: {exc}")
        return None


def read_summary_dict(wb):
    """Return a flat key→value dict from the Summary sheet, skipping section headers."""
    if wb is None or 'Summary' not in wb.sheetnames:
        return {}
    ws = wb['Summary']
    skip = {'', 'System Info', 'Model Config', 'Physics Params', 'Training Performance'}
    data = {}
    for row in ws.iter_rows(values_only=True):
        k = str(row[0]).strip() if row[0] is not None else ''
        if k and k not in skip and len(row) > 1 and row[1] is not None:
            data[k] = row[1]
    return data


def read_loss_history(wb):
    """Return (epochs, totals) lists from the Loss History sheet."""
    if wb is None or 'Loss History' not in wb.sheetnames:
        return [], []
    ws = wb['Loss History']
    epochs, totals = [], []
    for i, row in enumerate(ws.iter_rows(values_only=True)):
        if i == 0:          # header row
            continue
        if row[0] is None:
            continue
        try:
            epochs.append(float(row[0]))
            totals.append(float(row[1]))
        except (TypeError, ValueError):
            pass
    return epochs, totals


def read_eval_results(wb):
    """Return list of (scenario_label, pos_error, vel_error) from Evaluation Results."""
    if wb is None or 'Evaluation Results' not in wb.sheetnames:
        return []
    ws = wb['Evaluation Results']
    results = []
    for i, row in enumerate(ws.iter_rows(values_only=True)):
        if i == 0:          # header row
            continue
        if row[0] is None:
            continue
        try:
            scenario = str(row[COL_SCENARIO])
            pos_err  = float(row[COL_POS_ERR])
            vel_err  = float(row[COL_VEL_ERR])
            results.append((scenario, pos_err, vel_err))
        except (TypeError, ValueError, IndexError):
            pass
    return results


def get_float(d, *keys):
    """Try multiple key variants in a dict; return float or None."""
    for k in keys:
        if k in d and d[k] is not None:
            try:
                return float(d[k])
            except (TypeError, ValueError):
                pass
    return None


def read_phase_times_from_profiling(wb):
    """Compute Adam and L-BFGS wall-clock times from the Training Profiling sheet.

    Both Python and Julia exports write per-epoch rows with a Phase column
    ('adam' / 'lbfgs') and a cumulative Wall Time column.  We derive:
        adam_time  = max wall_time among adam rows
        lbfgs_time = max wall_time overall - adam_time
    Returns (adam_time, lbfgs_time); either may be None if data is absent.
    """
    if wb is None or 'Training Profiling' not in wb.sheetnames:
        return None, None
    ws = wb['Training Profiling']

    # Locate Phase and Wall-Time columns from the header row
    header = [c.value for c in next(ws.iter_rows(min_row=1, max_row=1))]
    phase_col = next(
        (i for i, h in enumerate(header)
         if h and 'phase' in str(h).lower()), None)
    wall_col = next(
        (i for i, h in enumerate(header)
         if h and 'wall' in str(h).lower() and 'time' in str(h).lower()), None)

    if phase_col is None or wall_col is None:
        return None, None

    adam_max  = 0.0
    total_max = 0.0
    for row in ws.iter_rows(min_row=2, values_only=True):
        phase = str(row[phase_col]).strip().lower() if row[phase_col] is not None else ''
        try:
            t = float(row[wall_col])
        except (TypeError, ValueError):
            continue
        if phase == 'adam':
            adam_max = max(adam_max, t)
        total_max = max(total_max, t)

    adam_time  = round(adam_max, 2)              if adam_max  > 0 else None
    lbfgs_time = round(total_max - adam_max, 2)  if total_max > adam_max else None
    return adam_time, lbfgs_time


# ─── Plot helpers ──────────────────────────────────────────────────────────────

def style_ax(ax, title, xlabel, ylabel):
    ax.set_facecolor(C_PANEL)
    ax.set_title(title, color=C_TEXT, fontsize=11, fontweight='bold', pad=10)
    ax.set_xlabel(xlabel, color=C_SILVER, fontsize=9, labelpad=5)
    ax.set_ylabel(ylabel, color=C_SILVER, fontsize=9, labelpad=5)
    ax.tick_params(colors=C_STEEL, labelsize=8)
    ax.grid(True, color=C_GRID, alpha=0.5, lw=0.5)
    for sp in ax.spines.values():
        sp.set_color(C_GRID)


def bar_value_labels(ax, bars, fmt='{:.2f}', max_ref=None):
    """Add value labels above each bar."""
    if max_ref is None:
        max_ref = max((b.get_height() for b in bars), default=1.0)
    offset = max_ref * 0.025
    for bar in bars:
        h = bar.get_height()
        if h > 0:
            ax.text(bar.get_x() + bar.get_width() / 2., h + offset,
                    fmt.format(h), ha='center', va='bottom',
                    color=C_TEXT, fontsize=7)


# ─── Main ──────────────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(
        description="UAV PINN Cross-Language Benchmark Comparison")
    parser.add_argument("--python-xlsx", default="pinn_benchmark_python.xlsx",
                        help="Path to the Python benchmark XLSX file")
    parser.add_argument("--julia-xlsx",  default="pinn_benchmark_julia.xlsx",
                        help="Path to the Julia benchmark XLSX file")
    args = parser.parse_args()

    print("\n" + "=" * 70)
    print("  UAV PINN Benchmark Comparison: Python (CUDA) vs Julia (MT-CPU)")
    print("=" * 70)
    print(f"  Python XLSX : {args.python_xlsx}")
    print(f"  Julia  XLSX : {args.julia_xlsx}")

    wb_py = load_xlsx(args.python_xlsx)
    wb_jl = load_xlsx(args.julia_xlsx)

    if wb_py is None and wb_jl is None:
        print("\n  [ERROR] Neither benchmark file was found.")
        print("  Run the training scripts first:")
        print("    python uav_pinn_v2_torch.py")
        print("    julia -t auto uav_pinn_v2_julia.jl")
        sys.exit(1)

    # ── Read all data ─────────────────────────────────────────────────────
    sum_py = read_summary_dict(wb_py)
    sum_jl = read_summary_dict(wb_jl)

    ep_py,  tot_py  = read_loss_history(wb_py)
    ep_jl,  tot_jl  = read_loss_history(wb_jl)

    eval_py = read_eval_results(wb_py)
    eval_jl = read_eval_results(wb_jl)

    # ── Timing: try multiple key spellings for cross-format compatibility ──
    total_py = get_float(sum_py, "Total Wall Time (s)", "Total Time(s)")
    total_jl = get_float(sum_jl, "Total Time(s)", "Total Wall Time (s)")
    adam_py  = get_float(sum_py, "Adam Phase Time (s)")
    lbfgs_py = get_float(sum_py, "L-BFGS Phase Time (s)")
    # Fallback: derive phase times from Training Profiling sheet
    if adam_py is None or lbfgs_py is None:
        _ap, _lp = read_phase_times_from_profiling(wb_py)
        if adam_py  is None: adam_py  = _ap
        if lbfgs_py is None: lbfgs_py = _lp
    adam_jl  = get_float(sum_jl, "Adam Phase Time (s)")
    lbfgs_jl = get_float(sum_jl, "L-BFGS Phase Time (s)")
    # Julia never writes phase times to Summary — always fall back
    if adam_jl is None or lbfgs_jl is None:
        _aj, _lj = read_phase_times_from_profiling(wb_jl)
        if adam_jl  is None: adam_jl  = _aj
        if lbfgs_jl is None: lbfgs_jl = _lj
    med_py   = get_float(sum_py, "Median Epoch Time (ms)", "Med Epoch(ms)")
    med_jl   = get_float(sum_jl, "Med Epoch(ms)", "Median Epoch Time (ms)")

    # ══════════════════════════════════════════════════════════════════════
    # Figure layout: 3 panels, dark background
    # ══════════════════════════════════════════════════════════════════════
    fig = plt.figure(figsize=(22, 7), facecolor=C_BG)
    fig.suptitle('UAV PINN Benchmark Comparison: Python (CUDA) vs Julia (MT-CPU)',
                 color=C_RED1, fontsize=15, fontweight='bold', y=0.98,
                 fontfamily='monospace')
    gs = gridspec.GridSpec(1, 3, figure=fig, wspace=0.38,
                           left=0.05, right=0.97, top=0.87, bottom=0.16)

    # ── Panel 1: Wall-Clock Time ───────────────────────────────────────────
    ax1 = fig.add_subplot(gs[0, 0])
    style_ax(ax1, 'Wall-Clock Time', '', 'Seconds / ms')

    time_labels = ['Total (s)', 'Adam (s)', 'L-BFGS (s)', 'Median Epoch (ms)']
    py_vals_raw = [total_py, adam_py, lbfgs_py, med_py]
    jl_vals_raw = [total_jl, adam_jl, lbfgs_jl, med_jl]

    # Keep only metrics where at least one side has data
    valid = [(lbl, pv, jv)
             for lbl, pv, jv in zip(time_labels, py_vals_raw, jl_vals_raw)
             if pv is not None or jv is not None]

    if valid:
        v_labels, v_py, v_jl = zip(*valid)
        v_py = [v if v is not None else 0.0 for v in v_py]
        v_jl = [v if v is not None else 0.0 for v in v_jl]
        x = np.arange(len(v_labels))
        w = 0.35
        bars_py = ax1.bar(x - w / 2, v_py, w, color=C_RED1, alpha=0.85,
                          label='Python (CUDA)', edgecolor='#CC1F1F', lw=0.8)
        bars_jl = ax1.bar(x + w / 2, v_jl, w, color=C_SILVER, alpha=0.80,
                          label='Julia (MT-CPU)', edgecolor=C_STEEL, lw=0.8)
        ax1.set_xticks(x)
        ax1.set_xticklabels(v_labels, rotation=18, ha='right',
                            fontsize=8, color=C_TEXT)
        max_h = max(max(v_py), max(v_jl), 1e-6)
        bar_value_labels(ax1, list(bars_py) + list(bars_jl), max_ref=max_h)
        ax1.legend(fontsize=8, facecolor=C_PANEL, edgecolor=C_GRID,
                   labelcolor=C_TEXT, loc='upper right')
    else:
        ax1.text(0.5, 0.5, 'No timing data\navailable',
                 transform=ax1.transAxes, ha='center', va='center',
                 color=C_STEEL, fontsize=11)

    # ── Panel 2: Loss Convergence (log scale) ─────────────────────────────
    ax2 = fig.add_subplot(gs[0, 1])
    style_ax(ax2, 'Loss Convergence', 'Epoch', 'Total Loss (log scale)')
    ax2.set_yscale('log')

    has_loss = False
    if ep_py and tot_py:
        ax2.plot(ep_py, np.maximum(tot_py, 1e-12),
                 color=C_RED1, lw=2, label='Python (CUDA)', alpha=0.9)
        has_loss = True
    if ep_jl and tot_jl:
        ax2.plot(ep_jl, np.maximum(tot_jl, 1e-12),
                 color=C_GOLD, lw=2, ls='--', label='Julia (MT-CPU)', alpha=0.9)
        has_loss = True

    if has_loss:
        ax2.legend(fontsize=8, facecolor=C_PANEL, edgecolor=C_GRID,
                   labelcolor=C_TEXT, loc='upper right')
    else:
        ax2.text(0.5, 0.5, 'No loss history\navailable',
                 transform=ax2.transAxes, ha='center', va='center',
                 color=C_STEEL, fontsize=11)

    # ── Panel 3: Final Accuracy across 4 scenarios ────────────────────────
    ax3 = fig.add_subplot(gs[0, 2])
    style_ax(ax3, 'Final Accuracy — 4 Scenarios', '', 'Error (m  /  m/s)')

    if eval_py or eval_jl:
        scen_names = ([e[0] for e in eval_py] if eval_py
                      else [e[0] for e in eval_jl])
        n_sc = len(scen_names)

        # Build arrays (fallback to 0 if a side is missing)
        pos_py = [e[1] for e in eval_py] if eval_py else [0.0] * n_sc
        vel_py = [e[2] for e in eval_py] if eval_py else [0.0] * n_sc
        pos_jl = [e[1] for e in eval_jl] if eval_jl else [0.0] * n_sc
        vel_jl = [e[2] for e in eval_jl] if eval_jl else [0.0] * n_sc

        x = np.arange(n_sc)
        w = 0.18          # 4 bars per group, slight gap between groups
        offsets = [-1.5 * w, -0.5 * w, 0.5 * w, 1.5 * w]

        b_pp = ax3.bar(x + offsets[0], pos_py, w, color=C_RED1,   alpha=0.85,
                       label='Python Pos (m)', edgecolor='#CC1F1F', lw=0.8)
        b_jp = ax3.bar(x + offsets[1], pos_jl, w, color=C_SILVER,  alpha=0.80,
                       label='Julia Pos (m)',  edgecolor=C_STEEL,   lw=0.8)
        b_pv = ax3.bar(x + offsets[2], vel_py, w, color=C_GOLD,    alpha=0.80,
                       label='Python Vel (m/s)', edgecolor='#A88040', lw=0.8)
        b_jv = ax3.bar(x + offsets[3], vel_jl, w, color=C_STEEL,   alpha=0.75,
                       label='Julia Vel (m/s)', edgecolor='#4A5A6D', lw=0.8)

        ax3.set_xticks(x)
        ax3.set_xticklabels(scen_names, rotation=20, ha='right',
                            fontsize=7, color=C_TEXT)

        all_vals = pos_py + pos_jl + vel_py + vel_jl
        max_h = max(all_vals) if all_vals else 1.0
        for bars in [b_pp, b_jp, b_pv, b_jv]:
            bar_value_labels(ax3, list(bars), fmt='{:.3f}', max_ref=max_h)

        ax3.legend(fontsize=7, facecolor=C_PANEL, edgecolor=C_GRID,
                   labelcolor=C_TEXT, loc='upper left', ncol=2)
    else:
        ax3.text(0.5, 0.5, 'No evaluation data\navailable',
                 transform=ax3.transAxes, ha='center', va='center',
                 color=C_STEEL, fontsize=11)

    # ── Save ──────────────────────────────────────────────────────────────
    out_path = "benchmark_comparison.png"
    fig.savefig(out_path, dpi=200, facecolor=C_BG, bbox_inches='tight')
    plt.close(fig)
    print(f"\n  Comparison chart saved -> {out_path}")
    print("  Done.\n")


if __name__ == "__main__":
    main()
